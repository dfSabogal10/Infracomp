package vos;

public class EmpleadoValue extends UsuarioValue {
	
	
	private double idEmpleado;
	
	private String login;
	
	private String password;
	
	private String cargo;
	
	private double idusuario;
	
	private double idoficina;

	public double getIdEmpleado() {
		return idEmpleado;
	}

	public void setIdEmpleado(double idEmpleado) {
		this.idEmpleado = idEmpleado;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCargo() {
		return cargo;
	}

	public void setCargo(String cargo) {
		this.cargo = cargo;
	}

	public double getIdusuario() {
		return idusuario;
	}

	public void setIdusuario(double idusuario) {
		this.idusuario = idusuario;
	}

	public double getIdoficina() {
		return idoficina;
	}

	public void setIdoficina(double idoficina) {
		this.idoficina = idoficina;
	}
	
	
	
	
	

}
