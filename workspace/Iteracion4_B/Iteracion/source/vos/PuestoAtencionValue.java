package vos;

public class PuestoAtencionValue {
	
	
	private double idPuesto;
	
	private String tipo;
	
	private String ubicacion;

	public double getIdPuesto() {
		return idPuesto;
	}

	public void setIdPuesto(double idPuesto) {
		this.idPuesto = idPuesto;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getUbicacion() {
		return ubicacion;
	}

	public void setUbicacion(String ubicacion) {
		this.ubicacion = ubicacion;
	}
	
	
	

}
