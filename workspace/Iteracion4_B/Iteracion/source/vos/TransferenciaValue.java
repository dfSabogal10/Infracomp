package vos;

public class TransferenciaValue extends OperacionValue{

private double idTranferencia;

private double idDestino;

private String nombreDestino;

public double getIdTranferencia() {
	return idTranferencia;
}

public void setIdTranferencia(double idTranferencia) {
	this.idTranferencia = idTranferencia;
}

public double getIdDestino() {
	return idDestino;
}

public void setIdDestino(double idDestino) {
	this.idDestino = idDestino;
}

public String getNombreDestino() {
	return nombreDestino;
}

public void setNombreDestino(String nombreDestino) {
	this.nombreDestino = nombreDestino;
}


	
}
